var primeiroNumero = parseInt(prompt("Digite o primeiro número:"));
var segundoNumero = parseInt(prompt("Digite o segundo número:"));

var soma = primeiroNumero + segundoNumero;
var subtracao = primeiroNumero - segundoNumero;
var multiplicacao = primeiroNumero * segundoNumero;
var divisao = primeiroNumero / segundoNumero;

console.log("resultado da soma: " + soma);
console.log("resultado da subtração: " + subtracao);
console.log("resultado da multiplicação: " + multiplicacao);
console.log("resultado da divisão: " + divisao);